/* –ö–æ–≥–¥–∞ –ø–ª–∏—Ç–∫–∏ –¥–æ–±–∞–≤–ª—è—é—Ç—Å—è/—É–¥–∞–ª—è—é—Ç—Å—è ¬´–Ω–∞ –≥–æ—Ä—è—á—É—é¬ª ‚Äî –ø–µ—Ä–µ—Å—Ç—Ä–∞–∏–≤–∞–µ–º */
const tilesMutObs = new MutationObserver((muts)=>{
  if (!isMobileMosaic()) return;
  let need = false;
  for (const m of muts){
    if (m.type === 'childList' && (m.addedNodes.length || m.removedNodes.length)){
      need = true; break;
    }
  }
  if (need) layoutMosaic();
});
const tm = tilesMain();
tm && tilesMutObs.observe(tm, { childList:true });

