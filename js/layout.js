function getTileAR(tile){
  const d = parseFloat(tile.dataset.ar);
  if (d && isFinite(d) && d > 0) return d;

  const v = tile.querySelector('video');
  const w = v?.videoWidth|0, h = v?.videoHeight|0;
  if (w>0 && h>0) return w/h;

  return tile.classList.contains('portrait') ? (9/16) : (16/9);
}

function getAvailableStageSize(m){
  const stage = m.closest?.('.stage') || m.parentElement || document.body;
  const cs = getComputedStyle(stage);
  const padV = (parseFloat(cs.paddingTop)||0) + (parseFloat(cs.paddingBottom)||0);
  const H = Math.max(0, stage.clientHeight - padV);
  const W = Math.max(0, m.clientWidth); // —à–∏—Ä–∏–Ω—É –±–µ—Ä—ë–º —É —Å–∞–º–æ–≥–æ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞ –ø–ª–∏—Ç–æ–∫
  return { W, H };
}

function layoutMosaic(){
  const m = tilesMain();
  if (!m) return;

  const tiles = Array.from(m.querySelectorAll('.tile'));
  const N = tiles.length;
  if (!N) return;

  const { W, H } = getAvailableStageSize(m);
  if (W < 10 || H < 10){ requestAnimationFrame(layoutMosaic); return; }

  // –ì—ç–ø –∏–∑ CSS –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π (–∏–ª–∏ –¥–µ—Ñ–æ–ª—Ç)
  const gap = parseFloat(getComputedStyle(m).getPropertyValue('--tile-gap')) || 10;

  // –ü–æ–¥–≥–æ—Ç–æ–≤–∏–º –¥–∞–Ω–Ω—ã–µ –ø–æ AR
  const items = tiles.map(t => ({ el:t, ar: Math.max(0.2, Math.min(5, getTileAR(t))) }));

  // –ö–æ–º—Ñ–æ—Ä—Ç–Ω—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã —Å—Ç—Ä–æ–∫: –∏–∑–±–µ–≥–∞–µ–º —Å–ª–∏—à–∫–æ–º —Ç–æ–Ω–∫–∏—Ö —Ä—è–¥–æ–≤
  const hardMinRowH = 56;                         // –∂—ë—Å—Ç–∫–∏–π –º–∏–Ω–∏–º—É–º
  const idealAvgRowH = Math.max(90, Math.min(220, H / Math.max(1, Math.round(Math.sqrt(N))))); // –ø—Ä–∏—è—Ç–Ω–∞—è ¬´—Å—Ä–µ–¥–Ω—è—è¬ª –≤—ã—Å–æ—Ç–∞

  const totalAR = items.reduce((s,x)=>s+x.ar, 0);
  let best = null;

  function measure(rowsCount){
    const target = totalAR / rowsCount;
    const rows = [];
    let row = [], sum = 0;

    for (let i=0;i<items.length;i++){
      const it = items[i];
      if (row.length===0){ row.push(it); sum = it.ar; continue; }

      const remain = items.length - i;
      const rowsLeft = rowsCount - rows.length;
      const mustBreak = remain <= (rowsLeft-1); // –Ω–∞–¥–æ –æ—Å—Ç–∞–≤–∏—Ç—å –ø–æ –æ–¥–Ω–æ–º—É –Ω–∞ –∫–∞–∂–¥—ã–π –æ—Å—Ç–∞–≤—à–∏–π—Å—è —Ä—è–¥
      const closer = Math.abs(sum + it.ar - target) <= Math.abs(sum - target);

      if (!mustBreak && closer){
        row.push(it); sum += it.ar;
      } else {
        rows.push(row);
        row = [it]; sum = it.ar;
      }
    }
    if (row.length) rows.push(row);

    // –í—ã—Å–æ—Ç–∞ –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–∏, —á—Ç–æ–±—ã –∑–∞–ø–æ–ª–Ω–∏—Ç—å —à–∏—Ä–∏–Ω—É W
    const heights = rows.map(r => {
      const sumAR = r.reduce((s,x)=>s+x.ar,0);
      const n = r.length;
      const wAvail = W - gap*(n-1);
      let h = wAvail / sumAR;
      h = Math.max(hardMinRowH, h);
      return h;
    });

    const totalH = heights.reduce((s,h)=>s+h,0) + gap*(rows.length-1);
    const avgH = heights.reduce((s,h)=>s+h,0) / heights.length;

    // –ë–∞–∑–æ–≤—ã–π —Å–∫–æ—Ä ‚Äî –±–ª–∏–∑–æ—Å—Ç—å –∫ –¥–æ—Å—Ç—É–ø–Ω–æ–π –≤—ã—Å–æ—Ç–µ + —à—Ç—Ä–∞—Ñ –∑–∞ ¬´—Ç–æ–Ω–∫–æ—Å—Ç—å¬ª
    const thinPenalty = Math.max(0, (idealAvgRowH - avgH)); // —á–µ–º —Ç–æ–Ω—å—à–µ, —Ç–µ–º –±–æ–ª—å—à–µ —à—Ç—Ä–∞—Ñ
    const score = Math.abs(H - totalH) + thinPenalty * 8;   // –≤–µ—Å –ø–æ–¥–±–∏—Ä–∞–ª—Å—è —ç–º–ø–∏—Ä–∏—á–µ—Å–∫–∏

    return { rows, heights, totalH, avgH, score };
  }

  for (let r = 1; r <= N; r++){
    const cand = measure(r);
    if (!best || cand.score < best.score) best = { ...cand, rowsCount:r };
  }
  if (!best) return;

  // –ê–±—Å–æ–ª—é—Ç–Ω–æ–µ –ø–æ–∑–∏—Ü–∏–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ
  m.style.position = 'relative';
  let y = 0;
  const px = (v)=> Math.round(v) + 'px';

  best.rows.forEach((row, ri) => {
    const h = best.heights[ri];
    let x = 0;
    row.forEach((it, i) => {
      const w = it.ar * h;
      const el = it.el;

      // –í–ê–ñ–ù–û: –≤–∫–ª—é—á–∞–µ–º border-box, —á—Ç–æ–±—ã —Ä–∞–º–∫–∏/–ø–∞–¥–¥–∏–Ω–≥–∏ –Ω–µ —Ä–∞–∑–¥—É–≤–∞–ª–∏ —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏–π —Ä–∞–∑–º–µ—Ä
      el.style.boxSizing = 'border-box';

      el.style.position = 'absolute';
      el.style.top  = px(y);
      el.style.left = px(x);
      el.style.width  = px(w);
      el.style.height = px(h);
      el.style.aspectRatio = ''; // —Ñ–∏–∫—Å–∏—Ä—É–µ–º—Å—è –Ω–∞ —è–≤–Ω—ã—Ö width/height

      x += w + gap;
    });
    y += h + gap;
  });

  // –≤—ã—Å–æ—Ç–∞ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞ (—É–±–∏—Ä–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π gap)
  m.style.height = px(Math.max(0, y - gap));

  // –ø–æ–¥—á–∏—Å—Ç–∏–º –∫–ª–∞—Å—Å—ã/–∏–Ω–ª–∞–π–Ω—ã, –∫–æ—Ç–æ—Ä—ã–µ –º–µ—à–∞–ª–∏ –±—ã
  tiles.forEach(t=>{
    t.classList.remove('spotlight','thumb');
  });

  // —Å–∫—Ä–æ–ª–ª–±–∞—Ä –Ω–∞ –º–æ–±–∏–ª–µ –Ω–µ –Ω—É–∂–µ–Ω, –Ω–æ –Ω–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π –æ–±–Ω–æ–≤–∏–º
  queueSbarUpdate();
}

/* --- —Å–±—Ä–æ—Å –º–æ–∑–∞–∏–∫–∏ (–∫–æ–≥–¥–∞ –≤—ã—Ö–æ–¥–∏–º –∏–∑ –º–æ–±–∏–ª–∫–∏) --- */
function clearMosaic(){
  const m = tilesMain(); if (!m) return;
  m.style.position = '';
  m.style.height   = '';
  const tiles = m.querySelectorAll('.tile');
  tiles.forEach(t=>{
    t.style.position = '';
    t.style.top = '';
    t.style.left = '';
    t.style.width = '';
    t.style.height = '';
    t.style.aspectRatio = '';
    t.style.boxSizing = '';
  });
}

